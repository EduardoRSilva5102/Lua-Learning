-- Comando para mostrar uma mensagem
print("oioi")

-- Comando para mostrar uma conta matemática
print(3 + 2)
-- Também funciona com Strings
print("3" + "2")
print(3 + "2")

-- Conta matemática com números decimais
print(0.25 + 0.75)


